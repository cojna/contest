solve :: Int -> Int -> U.Vector Int -> Int
solve n k xs = head $ filter validate ds
  where
    !s = U.sum xs
    ds = L.sortBy (flip compare) $ divisors s
    validate d = sum (take (n-quot ss d) xs') <= k
      where
        xs' = L.sort.U.toList $ U.map (flip rem d) xs
        !ss = sum xs'

