#define MOD 1000000007

main :: IO ()
main = do
    q <- readLn @Int
    qs <- U.unfoldrN q (runParser $ (,) <$> int <*> int) <$> C.getContents
    putStr.unlines.map show.U.toList $ solve q qs

lim :: Int
lim = 100100

solve :: Int -> U.Vector (Int, Int) -> U.Vector Int
solve q qs = U.map (\(l, r) -> table U.! r - table U.! (l - 1)) qs
  where
    go (x:xs) (y:ys) = case compare x (div (y + 1) 2) of
        LT -> go xs (y:ys)
        EQ -> y : go xs ys
        GT -> go (x:xs) ys
    go _ _ = []
    !table
        = U.postscanl' (+) 0
        . U.accum (+) (U.replicate lim 0)
        . map (flip (,) 1)
        $ go smallPrimes smallPrimes

smallPrimes :: [Int]
smallPrimes = 2 : [ n | n<-[3,5..lim], all ((>0).rem n) $ takeWhile (\x->x*x<=n) smallPrimes]

