main :: IO ()
main = do
    n <- readLn :: IO Int
    xys <- U.unfoldrN n (runParser $ (,) <$> int <*> int) <$> C.getContents
    print $ solve n xys

solve :: Int -> U.Vector (Int, Int) -> Int
solve n xys
    = longestIncreasingSubsequence
    . U.map encodeLIS
    . radixSort64
    . U.map encodeSort
    $ xys <> U.map swap xys
  where
    maxY :: Int
    maxY = 1000000000
    encodeSort :: (Int, Int) -> Word64
    encodeSort (x, y) = unsafeCoerce $ unsafeShiftL x 32 .|. (maxY - y)
    encodeLIS :: Word64 -> Int
    encodeLIS w = negate (unsafeCoerce w .&. 0xffffffff - maxY)

longestIncreasingSubsequence :: U.Vector Int -> Int
longestIncreasingSubsequence xs = IS.size $ U.foldl' step IS.empty xs
  where
    step s x = case IS.lookupGE x s of
        Just y | x < y     -> IS.insert x $ IS.delete y s
               | otherwise -> s
        Nothing            -> IS.insert x s

radixSort64 :: U.Vector Word64 -> U.Vector Word64
radixSort64 v = F.foldl' step v [0, 16, 32, 48]
  where
    mask k x = fromIntegral $ unsafeShiftR x k .&. 0xffff
    step v k = U.create $ do
        pref <- U.unsafeThaw
            . U.prescanl' (+) 0
            . U.unsafeAccumulate (+) (U.replicate 0x10000 0)
            $ U.map (flip (,) 1 . mask k) v
        res <- UM.unsafeNew $ U.length v
        U.forM_ v $ \x -> do
            let !masked = mask k x
            i <- UM.unsafeRead pref masked
            UM.unsafeWrite pref masked $ i + 1
            UM.unsafeWrite res i x
        return res
{-# INLINE radixSort64 #-}
