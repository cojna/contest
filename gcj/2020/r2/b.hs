main :: IO ()
main = withGCJ $ do
    [c, d] <- map read.words <$> getLine :: IO [Int]
    xs <- map read.words <$> getLine :: IO [Int]
    es <- replicateM d $ do
        [u,v] <- map read.words <$> getLine :: IO [Int]
        return (u - 1, v - 1)
    putStrLn.unwords.map show $ solve c d xs es

inf :: Int
inf = 0x3f3f3f3f3f3f3f3f

solve :: Int -> Int -> [Int] -> [(Int, Int)] -> [Int]
solve n m xs es = runST $ do
    mat <- UM.replicate (n * n) 999999
    dist <- UM.replicate n inf
    UM.write dist 0 0
    time <- UM.replicate 1 (1 :: Int)
    let add t v = do
            U.forM_ (gr `adj` v) $ \nv -> do
                dv <- UM.read dist v
                dnv <- UM.read dist nv
                when (dv == inf && dnv < inf) $ do
                    UM.write dist v t
                    UM.write mat (ix v nv) (t - dnv)
                    UM.write mat (ix nv v) (t - dnv)
    let go !time !size !acc vvs@((sz,v):vs) uus@((t,u):us)
          | size == sz = add time v >> go time size (acc+1) vs uus
          | t == time = add time u >> go time size (acc+1) vvs us
          | acc > 0 = go (time+1) (size + acc) 0 vvs uus
          | otherwise = add t u >> go t size 1 vvs us
        go !time !size !acc vvs@((sz,v):vs) []
              | size == sz = add time v >> go time size (acc+1) vs []
              | otherwise = go (time+1) (size + acc) 0 vvs []
        go !time !size !acc [] uus@((t,u):us)
              | t == time = add time u >> go time size (acc+1) [] us
              | acc > 0 = go (time+1) (size + acc) 0 [] uus
              | otherwise = add t u >> go t size 1 [] us
        go _ _ _ [] [] = return ()
    go 1 1 0 xs1 xs0
    mapM (UM.read mat . uncurry ix) es
  where
    !gr = buildUndirectedGraph n (U.fromListN m es)
    (xs0, xs1)
        = bimap L.sort (L.sort . map (first negate))
        . L.partition ((>0) . fst)
        $ zip xs [1..]
    ix i j = i * n + j



