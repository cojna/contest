formatGCJ :: Int -> String
formatGCJ i = "Case #" <> shows i ": "
withGCJ :: IO () -> IO ()
withGCJ f = getArgs >>= \case { ["--debug"] -> f; [] -> do { t <- readLn; mapM_ ((*> f) . putStr . formatGCJ) [1 .. t]}; args -> error $ show args}

